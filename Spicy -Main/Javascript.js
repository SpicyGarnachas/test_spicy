function mouseOver() {
    document.getElementById("path001").setAttribute("style", "fill: rgb(38.819885%,0%,0%);");
    document.getElementById("path002").setAttribute("style", "fill: rgb(38.819885%,0%,0%);");
}

function mouseOut() {
    document.getElementById("path001").setAttribute("style", "fill: black;");
    document.getElementById("path002").setAttribute("style", "fill: black;");
}